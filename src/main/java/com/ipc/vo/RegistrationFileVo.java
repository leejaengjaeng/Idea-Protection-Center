package com.ipc.vo;

public class RegistrationFileVo {
	int rfid;
	int start_rid;
	int rid;
	String file_path;
	
	public int getRfid() {
		return rfid;
	}
	public void setRfid(int rfid) {
		this.rfid = rfid;
	}
	public int getStart_rid() {
		return start_rid;
	}
	public void setStart_rid(int start_rid) {
		this.start_rid = start_rid;
	}
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	
	

}
