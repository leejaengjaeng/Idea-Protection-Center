package com.ipc.vo;

public class CopyrightAdminVo {

	private int    cid;
	private String id;
	private String inventor_name;
	private String pl_name;
	private String email;
	private String title;
	private String reg_condition;
	
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInventor_name() {
		return inventor_name;
	}
	public void setInventor_name(String inventor_name) {
		this.inventor_name = inventor_name;
	}
	public String getPl_name() {
		return pl_name;
	}
	public void setPl_name(String pl_name) {
		this.pl_name = pl_name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getReg_condition() {
		return reg_condition;
	}
	public void setReg_condition(String reg_condition) {
		this.reg_condition = reg_condition;
	}
	
	
}
